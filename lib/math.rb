def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  subtract_num = num1 - num2
  subtract_num
end

def division(num1, num2)
  div_final = num1 / num2
  div_final

end

def multiplication(num1, num2)
  mult_final = num1 * num2
  mult_final

end

